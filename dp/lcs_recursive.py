a = input()
b = input()
n = len(a)
m = len(b)

#################################################################
# Recursive Method
#################################################################
def lcs(a, b, n, m):
    if n == 0 or m == 0:
        return 0

    if a[n-1] == b[m-1]:
        return 1 + lcs(a, b, n-1, m-1)

    else:
        return max(lcs(a, b, n-1, m), lcs(a, b, n, m-1))

print(lcs(a, b, n, m))